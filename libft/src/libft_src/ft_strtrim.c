#include "../../libft.h"

char	*ft_strtrim(char const *s1, char const *set)
{
	char	*output;
	size_t	start;
	size_t	end;
	size_t	len;

	if (!s1 || !set)
		return (NULL);
	start = 0;
	end = ft_strlen(s1);
	while (s1[start] && ft_strchr(set, s1[start]))
		start++;
	while (end > start && ft_strchr(set, s1[end - 1]))
		end--;
	len = end - start;
	output = (char *)malloc(sizeof(char) * len + 1);
	if (!output)
		return (NULL);
	ft_strlcpy(output, s1 + start, len + 1);
	return (output);
}
